/*
Write a program to print the following pattern:
1111
2222
3333
4444 */
public class Square_number {
    public static void main(String[] args) {
        
        for (int i=0; i<4; i++){
            System.out.println();
            for(int j=0; j<4; j++){
                System.out.print(i+1);
            }
        }
    }
    
}
